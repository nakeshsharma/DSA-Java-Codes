import java.util.*;
public class logical {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.println( (5>2) & (45>21) );  // &&= logical AND true if both are true else false
        
        System.out.println( (7>2) | (3<1)  );  //  ||=logical OR false if both are flase else true
        
        System.out.println( !(7>2)   );  // != logical NOT
    
    }

    
}
